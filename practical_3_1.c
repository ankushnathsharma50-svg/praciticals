#include <stdio.h>

int main() {
    
    printf("ankush nath sharma\n");
    printf("ERP :- 11809\n\n");

    
    int i = 10;
    float f = 12.34f;
    char c = 'A';
    double d = 123.456789;

    
    printf("Integer value (%%d)   = %d\n", i);
    printf("Float value (%%f)     = %.2f\n", f);
    printf("Character value (%%c) = %c\n", c);
    printf("Double value (%%lf)   = %.6lf\n", d);

    return 0;
}
